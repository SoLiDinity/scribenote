const CACHE_NAME = `notes-app-cache-${new Date().getTime()}`;
const STATIC_ASSETS_PATH = '/static/';
const TEMPLATES_ASSETS_PATH = '/templates/';

self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => {
      return cache.addAll(['/']);
    })
  );
  self.skipWaiting();
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches.keys().then((cacheNames) => {
      return Promise.all(
        cacheNames.map((cacheName) => {
          if (cacheName !== CACHE_NAME) {
            return caches.delete(cacheName);
          }
        })
      );
    })
  );
  self.clients.claim();
});

self.addEventListener('fetch', (event) => {
  const requestUrl = event.request.url;

  if (requestUrl.includes(STATIC_ASSETS_PATH) || requestUrl.includes(TEMPLATES_ASSETS_PATH)) {
    event.respondWith(
      caches.match(event.request).then((response) => {
        return (
          response ||
          fetch(event.request).then((fetchResponse) => {
            return caches.open(CACHE_NAME).then((cache) => {
              cache.put(event.request, fetchResponse.clone());
              return fetchResponse;
            });
          })
        );
      })
    );
  } else {
    event.respondWith(fetch(event.request));
  }
});
